<p style="padding: 30px;text-align: justify;line-height: 2;color: #808080;font-size: 16px;">Chapter 6 of the Companies Act 2008, provides for the efficient rescue and recovery of financially distressed companies in what is referred to as Business Rescue proceedings. Indicators of financial distress::</p>
<p style="padding-left: 20px;text-align: justify;line-height: 2;color: #808080;font-size: 16px;">  &#10687; The company is unlikely to meet its debt obligations as they become due in the next 6 months</p>
<p style="padding-left: 20px;text-align: justify;line-height: 2;color: #808080;font-size: 16px;">  &#10687; The company is likely to be insolvent in the next 6 months.</p>
<p style="padding: 30px;text-align: justify;line-height: 2;color: #808080;font-size: 16px;">Business Rescue proceedings can be voluntary and initiated by a Board resolution or through an order of Court either by the company or through an application by an affected person. The decision to pass a resolution for Business Rescue needs to be done urgently to enable the Business Rescue Practitioner (BRP) to be appointed. The BRP oversees and takes control of the management, affairs and business of the company on a temporary basis in order to develop a Business Rescue Plan.

The objective is to devise a superior solution for affected persons (creditors, shareholders, employees and trade unions) than would be the case in a liquidation. The plan will be implemented if approved by creditors and shareholders to the extent that the rights of shareholders will be affected.</p>
