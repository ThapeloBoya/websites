<h2>What are the signs of financial distress that should cause the shareholders and directors to consider appointing a Turnaround Specialist or Business Rescue Practitioner?</h2>
<i class="material-icons add"><img src="images/add.png" style="width: 30px;"></i>
<i class="material-icons remove"><img src="images/minus.png" style="width: 30px;"></i>
</div>
<div class="accordion-content">
<p>The Companies Act definition is narrow in so far as it relates to when companies should file for Business Rescue. In terms of section 128, financial distress means that the company is unlikely to meet all its debt obligations as they become due in the next six months and there is a reasonable likelihood that the company will be insolvent in the next six months. 
The signs of financial distress are however much wider and it is important for Boards of Companies to act in time to avoid the need for Business Rescue and failing which liquidation of the business. Some of the indicators include:</p>
<ul>
<li>• Declining revenues including loss of key contracts</li>
<li>• Decline in operating margins</li>
<li>• Increase in accounts receivable days</li>
<li>• Increase in accounts payable days</li>
<li>• Build-up of inventories</li>
<li>• Lack of liquidity / exceeding banking facilities</li>
<li>• Industrial action, staff and management turnover</li>
<li>• Failure to meet debt obligations including a breach in debt covenants</li>
<li>• Unresolved redemption of debt that its due in the medium term</li>
<li>• Lack of adequate and timely financial and IFRS reporting</li>
<li>• Absence of management accounts and budgets monitoring </li>
<li>• Downgrading of the company by its credit rating agency</li>
</ul>
