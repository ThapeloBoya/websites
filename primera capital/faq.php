<div class="accordion-title">
<h2>Please explain Business Rescue in more detail?</h2>
<i class="material-icons add"><img src="images/add.png" style="width: 30px;"></i>
<i class="material-icons remove"><img src="images/minus.png" style="width: 30px;"></i>
</div>
<div class="accordion-content">
<p>Business Rescue is a process provided for in terms of Section 128 of the Companies Act. It is proceedings to facilitate the rehabilitation of a company that is financial distressed by providing for:</p>
<ul>
  <li>• The temporary supervision of the company and of the management of its affairs, business and property</li>
  <li>• A temporary moratorium on the rights of claimants against the company or in respect of property in its possession </li>
  <li>• The development and implementation, if approved, of a plan to rescue the company or, if that is not possible a plan that would achieve a better return for the company’s creditors that the payment they would receive if the company had simply been liquidated immediately.</li>
</ul>
<p>Business Rescue proceedings may be instituted upon the passing of a resolution by the directors of the company which could also be endorsed by the court. It could also be initiated by Court Action by an Affected Person which includes members of the company, creditors and employees. The company files a Notice of Commencement of Business Rescue Proceedings in the prescribed manner with the Companies and Intellectual Property Commission (“CIPC”). This is followed by the appointment of a Business Rescue Practitioner in the prescribed form with the CIPC within 5 Business Days. <br><br>Notices are required to be sent to affected persons as prescribed. The BRP is required to engage with Affected Persons and publish the Business Rescue Plan within 25 business days from appointment unless extension is obtained from creditors or the Court. If the Business Rescue proceedings have not been terminated after three months, the BRP is required to provide progress reports to the CIPC in the prescribed form. The proceedings end in a number of circumstances including the filing of a notice of termination by the BRP or a notice of substantial implementation.</p>
</div>
