<h2>What is the difference between a Chief Restructuring Officer (CRO) and a Business Rescue Practitioner?</h2>
<i class="material-icons add"><img src="images/add.png" style="width: 30px;"></i>
<i class="material-icons remove"><img src="images/minus.png" style="width: 30px;"></i>
</div>
<div class="accordion-content">
<p>The CRO and BRP are appointed in different circumstances. The CRO (who may also be a registered Business Rescue Practitioner) is appointed by the Board of the company or its funders and investors in an informal turnaround situation. The BRP is appointed by the company by filing a notice of appointment with CIPC. The BRP has wide ranging powers and effectively takes over full management of the company from the Board and Management. The BRP may only be removed from office by an order of Court in terms of section 130 and section 139. The BRP becomes an Officer of the Court and has prescribed responsibilities in terms of the Act. <br><br>CRO is a term borrowed from the UK and the US where it is more prevalent and refers to an Executive appointed on a temporary basis by the company to lead the Turnaround of the business.  Restructuring is a critical activity that requires strong and dedicated leadership to lead the company from financial distress to financial stability. The CRO is the leader of the restructuring and has three main responsibilities being to (a) bring credibility and objectivity to the restructuring process (b) driving and creating stability to the entire restructuring process (c) building consensus amongst stakeholders about the direction of the restricting. </p>
